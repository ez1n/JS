/** 객체 타입 */

// Heap(메모리)에 저장됨

let name = "apple";
let color = "red";
let display = "🍎";

let apple = {
  name: "사과",
  color: "red",
  display: "🍎",
};

console.log(apple); // { name: '사과', color: 'red', display: '🍎' }
console.log(apple.name); // 사과
console.log(apple.color); // red
console.log(apple.display); // 🍎

let orange = {
  name: "오렌지",
  color: "orange",
  display: "🍊",
};

console.log(orange); //{  name: 'orange', color: 'orange', display: '🍊' }
